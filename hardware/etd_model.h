inline float etd_dequantize(int16_t val, float min_val, float max_val) {
    return min_val + (val / 32767.0f) * (max_val - min_val);
}

float etd_traverse_tree(uint8_t tree_idx, float features[ETD_N_FEATURES]) {
    Node node;
    uint16_t node_idx = 0;
    uint16_t tree_size = pgm_read_word(&etd_tree_sizes[tree_idx]);
    const Node* tree = (const Node*)pgm_read_ptr(&etd_trees[tree_idx]);
    
    while (node_idx < tree_size) {
        memcpy_P(&node, &tree[node_idx], sizeof(Node));
        
        if (node.feature_idx == -1) {
            return etd_dequantize(node.value, 
                pgm_read_float(&etd_value_min),
                pgm_read_float(&etd_value_max));
        }
        
        float threshold = etd_dequantize(node.threshold,
            pgm_read_float(&etd_feature_mins[node.feature_idx]),
            pgm_read_float(&etd_feature_maxs[node.feature_idx]));
        
        if (features[node.feature_idx] <= threshold) {
            node_idx = node.left_idx;
        } else {
            node_idx = node.right_idx;
        }
    }
    
    return 0.0f;
}

float predict_etd(float raw_features[ETD_N_FEATURES]) {
    float features[ETD_N_FEATURES];
    for (uint8_t i = 0; i < ETD_N_FEATURES; i++) {
        float mean = pgm_read_float(&etd_feature_means[i]);
        float std = pgm_read_float(&etd_feature_stds[i]);
        features[i] = (raw_features[i] - mean) / std;
    }
    
    float prediction = 0.0f;
    for (uint8_t i = 0; i < ETD_N_TREES; i++) {
        prediction += etd_traverse_tree(i, features);
    }
    
    return prediction / ETD_N_TREES;
}

